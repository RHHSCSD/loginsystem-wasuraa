    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        // TODO add your handling code here:
        if(username.getText().equals("") || password.getText().equals("")) { // Only need to check relevant fields for emptiness
            loginText.setForeground(Color.red);
            loginText.setText("Empty fields found");
        } else if(username.getText().contains(" ")) { // Only need to check relevant fields for spaces
            loginText.setForeground(Color.red);
            loginText.setText("Username contains space");
        } else {
            // Try to login
            boolean success = regSys.login(username.getText(), password.getText());
            // Provide feedback to the user
            if(success) {
                loginText.setForeground(Color.green);
                loginText.setText("Login to " + username.getText() + " successful!");
            }
            else {
                loginText.setForeground(Color.red);
                loginText.setText("Login Invalid");
            }
        }
    }//GEN-LAST:event_loginButtonMouseClicked

