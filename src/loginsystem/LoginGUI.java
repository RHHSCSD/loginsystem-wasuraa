    private void registerButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerButtonMouseClicked
        // TODO add your handling code here:
        // Check fields are filled properly, perform password checks and provide necessary user feedback
        if(checkSpaces()) {
            regText.setForeground(Color.red);
            regText.setText("Invalid spaces found");
        } else if(checkEmpty()) {
            regText.setForeground(Color.red);
            regText.setText("Empty fields found");
        } else if(RegistrationSystem.failedPassRequirements(password.getText())) {
            regText.setForeground(Color.red);
            regText.setText("Password should contain minimum 8 characters, upper/lower case, number and special character");
        } else if(RegistrationSystem.matchingWeakPass(password.getText())) {
            regText.setForeground(Color.red);
            regText.setText("Weak Password");
        } else {
            // Try to register
            boolean success = regSys.register(username.getText(), password.getText(), firstName.getText(), lastName.getText(), email.getText());
            // Provide feedback to the user
            if(success) {
                regText.setForeground(Color.green);
                regText.setText("Success!");
            }
            else {
                regText.setForeground(Color.red);
                regText.setText("Matching username or email detected");
            }
        }
    }//GEN-LAST:event_registerButtonMouseClicked

